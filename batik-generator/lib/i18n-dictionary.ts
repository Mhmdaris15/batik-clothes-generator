function deepGet(source: Record<string, unknown>, path: string): unknown {
  return path.split(".").reduce<unknown>((current, key) => {
    if (!current || typeof current !== "object") return undefined;
    return (current as Record<string, unknown>)[key];
  }, source);
}

export function formatMessage(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return Object.entries(vars).reduce((acc, [key, value]) => {
    return acc.replace(new RegExp(`\\{${key}\\}`, "g"), String(value));
  }, template);
}

export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const primary = deepGet(dictionaries[lang] as Record<string, unknown>, key);
  const fallback = deepGet(dictionaries.en as Record<string, unknown>, key);
  const value = typeof primary === "string" ? primary : typeof fallback === "string" ? fallback : key;
  return formatMessage(value, vars);
}

export function isSupportedLang(value: string | undefined): value is Lang {
  if (!value) return false;
  return supportedLanguages.includes(value as Lang);
}
